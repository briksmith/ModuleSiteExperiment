public class ComplexNumber {

    private double real;
    private double imaginary;

    public ComplexNumber(double inReal, double inImaginary){

        real = inReal;
        imaginary = inImaginary;
    }

    public double getReal() {
        return real;
    }

    public double getImaginary() {
        return imaginary;
    }
}
